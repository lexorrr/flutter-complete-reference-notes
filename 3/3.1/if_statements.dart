void main() {
  // 3 Control flow and functions
  // 3.1 If statement
  // This is probably the most famous statement of any programming language
  // and in Dart it works exactly as you would expect. The else is optional
  // and it can be omitted when not needed.
  // You can avoid using brackets in case of one-liner statements.
  final random = 13;
  if (random % 2 == 0)
    print("Got an even number");
  else
    print("Got an odd number");
}